#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*str;
	size_t	i;

	str = b;
	i = 0;
	while (len > 0)
	{
		str[i] = c;
		i++;
		len--;
	}
	return (b);
}
