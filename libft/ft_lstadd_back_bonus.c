#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	if (!new)
		return ;
	if (!lst)
		*lst = new;
	else
	{
		while (*lst)
			lst = &(*lst)->next;
		*lst = new;
	}
}
